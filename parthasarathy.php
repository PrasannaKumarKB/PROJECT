            <h3>HISTORY</h3>
            <p>The temple was originally built by the Pallavas in the 8th century, subsequently expanded by Cholas and later by the Vijayanagara kings in the 15th century.The temple has several inscriptions dating from the 8th century in Tamil presumably from the period of Dantivarman, who was a Vishnu devotee.Thirumangai Alvar, the 9th-century poet-saint also attributes the building of temple to the Pallava king.From the internal references of the temple, it appears that the temple was restored during 1564 CE when new shrines were built.In later years, endowments of villages and gardens have enriched the temple.The temple also has inscriptions about the Pallava king, Nandivarman of the 8th Century. The temple was extensively built during the Chola period and a lot of inscriptions dating back to the same period are found here. The outer most mandapam is replete with sculptures of various forms of Vishnu, especially the avatars. One can also see inscriptions of Dantivarma Pallava of the 8th century, Chola and Vijayanagara in the temple. The first architectural expansion of the temple took place during the reign of the Pallavas (Tondaiyar Kon) as vividly described by Tirumangai Alvar. Reminiscent of this is the inscription of the Pallava King Dantivarman (796â€“847 CE), which is preserved in the temple.</p>
