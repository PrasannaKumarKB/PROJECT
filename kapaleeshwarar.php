            <h3>HISTORY</h3>
            <p>The temple's name is derived from the words kapāla (head) and Īśvara, an epithet of Shiva. According to the Puranas, during the meeting of Brahma and Shiva at top of Mount Kailash Brahma failed to show the due respect to Shiva. Due to this, Shiva plucked off one of Brahma's heads (kapalams). In an act of penance, Brahma came down to the site of Mylapore and installed a lingam to please Shiva.This place is known as Sukra Puri, Veda Puri, among many other names including "Kailaye Mayilai" and "Mayilaye Kayilai" (lit. 'Mylapore is Kailash'). According to regional legend, the goddess Karpagambal, a form of Shiva's consort Parvati, due to a curse became a pea-hen and did penance here to get back her original personality. Shiva's son Murugan received the spear (vel) for the destruction of a demon from Parvati here. Brahma had worshipped here to get rid of his ego and get back his power to create. The four Vedas have worshipped here. Shukra worshipped Shiva here and got back his lost eye. Rama has worshipped here and won the war against Ravana and brought back Sita from Lanka. The daughter of Sivanesa Chettiar Angam Poompavai got her lost life due to snake bite was later resurrected here by the powers of Thirugnana Sambandar.Vayilar Nayanar, a saiva saint, attained salvation here and Mylapore is also the birth place of Thiruvalluvar who wrote the Thirukkural.The site is glorified in the text Tevaram. Sambandar and Arunagirinathar extolled the goddess Karpagambal in their works. The 10-day festival during March / April is a treat to watch and Arubathu Moovar festival is attended by lakhs of devotees every year.</p>
